export const lookAt = (cameraPosition:Vector3, target:Vector3, up:Vector3, dst?:Float32Array):Float32Array=> {
    dst = dst || new Float32Array(16);
    var zAxis = vectorNormalize(
        vectorSubtract(cameraPosition, target));
    var xAxis = vectorNormalize(vectorCross(up, zAxis));
    var yAxis = vectorNormalize(vectorCross(zAxis, xAxis));

    dst[ 0] = xAxis[0];
    dst[ 1] = xAxis[1];
    dst[ 2] = xAxis[2];
    dst[ 3] = 0;
    dst[ 4] = yAxis[0];
    dst[ 5] = yAxis[1];
    dst[ 6] = yAxis[2];
    dst[ 7] = 0;
    dst[ 8] = zAxis[0];
    dst[ 9] = zAxis[1];
    dst[10] = zAxis[2];
    dst[11] = 0;
    dst[12] = cameraPosition[0];
    dst[13] = cameraPosition[1];
    dst[14] = cameraPosition[2];
    dst[15] = 1;

    return dst;
}

